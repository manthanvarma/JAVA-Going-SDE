public class while_loop {
    public static void main(String[] args) {
        int i = 1;
        while(i<=5) {
            System.out.println(i);
            i++;
        }

        int j = 5;
        while(j<=10) {
            System.out.println(j);
            j=j*2;
        }

        int k = 1;
        while(k<=5) {
            System.out.println(k);
            k=k+2;
        }
    }
}